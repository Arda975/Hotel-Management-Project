
public class Hotel {
	String name;
	String foundation_date;
	String address;
	String phone_number;
	String star;
	public Hotel(String name, String foundation_date, String address, String phone_number, String star) {
		
		this.name = name;
		this.foundation_date = foundation_date;
		this.address = address;
		this.phone_number = phone_number;
		this.star = star;
	}
	
}
